def loop_message_five_times(message)
  counter = 0
  while counter < 5 do
    puts message
    counter++
  end
end

def 
  loop_message_n_time(message, limit) do
  counter = 0
  while counter < limit
    puts message
    counter++
  end
  }
end

def output_array(array) do
  counter = 0 
  while counter < array.length
    puts array[counter]
    counter++
  end
end
  